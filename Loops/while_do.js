// WHILE LOOP
// Runs as long as the condition is TRUE

let i = 1;            // initialization

while (i <= 5) {      // condition
    console.log("Kirtan");
    i++;              // updation
}

// DO-WHILE LOOP
// Runs at least ONCE even if condition is false

let j = 1;            // initialization

do {
    console.log("Kirtan");
    j++;              // updation
} while (j <= 5);     // condition
